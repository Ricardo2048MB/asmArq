EXTRN DALE_ENTER: PROC;CARRIAGE RETURN, LINE FEED (RETORNO DE CARRO, DALTO DE LINEA) [ES DALE_ENTER]
EXTRN MUESTRA_LINEA: PROC
PUBLIC DISP_MEDIO_SECTOR
DISP_MEDIO_SECTOR PROC
	MOV AX, @DATA
	MOV DS, AX
	XOR DX, DX
	MOV CX, 16
MEDIO_SECTOR:
	CALL MUESTRA_LINEA
	CALL DALE_ENTER
	ADD DX, 16
	LOOP MEDIO_SECTOR
	.EXIT
DISP_MEDIO_SECTOR ENDP
