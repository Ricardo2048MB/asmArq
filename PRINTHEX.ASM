.MODEL SMALL
.CODE
	MOV DL, 3FH
	MOV DH, DL
	MOV CX, 4H
	SHR DL, CL
	CMP DL, 10
	JAE HEX_LETTER
	ADD DL, "0"
	JMP SHORT WRITE_DIGIT
HEX_LETTER:
	ADD DL, "A"-10
WRITE_DIGIT:
	CALL WRITE_CHAR
	MOV DL, DH
	AND DL, 0FH
	CMP DL, 10
	JAE HEX_LETTER1
	ADD DL, "0"
	JMP SHORT WRITE_DIGIT2
HEX_LETTER1:
	ADD DL, "A"-10
WRITE_DIGIT2:
	CALL WRITE_CHAR
.EXIT
	PUBLIC WRITE_CHAR
	WRITE_CHAR PROC
	PUSH AX
	MOV AH, 02
	INT 21H
	POP AX
	RET
	WRITE_CHAR ENDP
END 
