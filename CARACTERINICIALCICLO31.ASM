.MODEL SMALL
.DATA
.STACK

	ENTRADA DB "INGRESA UN CARACTER","$"
	
.CODE

	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,ENTRADA
	INT 21H
	
	MOV AH,01
	INT 21H
	
	MOV DL,AL
	MOV CX,1F; 1F SIGNIFICA 31 EN DECIMAL
	MOV AH,02
	
ESTA:
	INT 21H
	INC DL
	LOOP ESTA; 31 VECES, O SEA, VA A IMPRIMIR EL CARACTER QUE SE LE DEE Y LOS 30 QUE LE SIGUEN
	
.EXIT
END
