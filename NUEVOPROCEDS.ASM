.MODEL SMALL
.DATA
	
	MENSAJE1 DB 10, 13, "MENSAJE1: $"
	MENSAJE2 DB 10, 13, "MENSAJE2: $"
	
.CODE
	
	MOV AX, @DATA; NO SE PUEDE CARGAR @DATA DIRECTAMENTE EN DS
	MOV DS, AX; POR ESA RAZOON PRIMERO HAY QUE CARGAR @DATA EN AX Y LUEGO PASAR LO QUE HAY AHÍ A DS
	MOV AH, 09; EESTE SERVICIO IMPRIME UNA CADENA QUE TERMINE EN $ Y QUE ESTEE EN DX
	LEA DX, MENSAJE1
	INT 21

	MOV AH, 09; EESTO SE REPITE PARA ASEGURARSE DE QUE NO SE HAYA CAMBIADO EL SERVICIO PERO, DE HECHO NO ES NECESARIO
	LEA DX, MENSAJE2
	INT 21
	
.EXIT
COMMENT !
;FUERA DEL .EXIT Y ANTES DEL END VAN LOS PROCEDIMIENTOS
	IMPRIME_CADENA PROC
		MOV AH, 09
		INT 21H
	RET
	ENDP
!
END
COMMENT!
EL PROGRAMA QUE HICIMOS HACE RATO
IMPRIME CADENA, CARACTER LEE CARACTER
PERO SE PUEDEN GENERAR MÁS
!


