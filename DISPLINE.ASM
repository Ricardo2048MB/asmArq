.MODEL SMALL
.STACK
.DATA
MSG1   DB  10,  13, "IMPRIME LINEAS", "$"
VECTOR DB 10H, 11H, 12H, 13H, 14H, 15H, 16H, 17H, 18H
       DB 19H, 1AH, 1BH, 1CH, 1DH, 1EH, 1FH
.CODE
EXTERN IMPRIME_HEXADECIMAL:PROC
EXTERN ESCRIBE_CARACTER:PROC
MOV AX, @DATA
MOV DS, AX
XOR BX, BX
MOV CX, 10H;10H = 16D, 0FH = 15D
ETIQUETA1:
MOV DH, 00H
MOV DL, VECTOR[BX]
CALL IMPRIME_HEXADECIMAL;UNO SOLO
MOV DL, " "
CALL ESCRIBE_CARACTER;ESCRIBIR CARACTER NORMAL
INC BX;BX+=1
LOOP ETIQUETA1
.EXIT
END
